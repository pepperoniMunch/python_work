# Removing an item using the pop() method, which lets you remove the last item from a list and then work with it

letters = ['a', 'b', 'c', 'd', 'e']
print(letters)

popped_letters = letters.pop()
print(letters)
print(popped_letters)

print(f"The last letter I typed out in the list was {popped_letters}.\n")

